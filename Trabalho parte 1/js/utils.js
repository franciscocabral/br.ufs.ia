var mapa = [];
var inicio = {x:0,y:0};
var fim = {x:23,y:23};
var bounds = {x:25,y:25};
var max = bounds.x * bounds.y;

function up(pos){
    return {x: pos.x, y: pos.y-1};
}
function right(pos){
    return {x: pos.x+1, y:pos.y};
}
function down(pos){
    return {x: pos.x, y:pos.y+1};
}
function left(pos){
    return {x: pos.x-1, y:pos.y};
}

function canWalk(pos){
    if(pos.x < bounds.x && pos.y < bounds.y){
        if(pos.x > -1 && pos.y > -1){
            if(mapa[pos.x][pos.y] != 1){
                return true;
            }
        }
    }
    return false;
}

var calcDistancia = function(p1, p2){
    return Math.sqrt(Math.pow(p1.x - p2.x, 2)+ Math.pow(p1.y - p2.y,2));
};
var calcManhattan = function(p1,p2){
    return Math.abs(p1.x - p2.x) + Math.abs(p1.y - p2.y);
};
var foiVisitado = function(pos){
    return (mapa[pos.x][pos.y] > 0);
};
var marcaVisita = function(pos){
    mapa[pos.x][pos.y] = 250;
};


function print(pos, color){
    if (typeof color == 'undefined') {
        color = null;
    }
    var id = "#x"+pos.x+"y"+pos.y;
    if(color == null && mapa[pos.x][pos.y] == 1) color = 'black';
    else if(color == null && mapa[pos.x][pos.y] != 0) {
        myColor = parseInt(mapa[pos.x][pos.y]/divColor);
        color = "rgba("+myColor+",0,0,1)";
    }
    $(id).css('background-color', color);
    return true;
}
function printMap(map){
    for(var i = 0; i < map.length; i++){
        for(var j = 0; j < map[i].length; j++){
            var pos = {x:i,y:j}
            var id = "x"+pos.x+"y"+pos.y;
            $("#myMap").append("<div id='"+id+"' class='myPos'>{"+pos.x+","+pos.y+"}</div>");
            if(pos.x == inicio.x && pos.y == inicio.y) print(pos, "blue");
            else if(pos.x == fim.x && pos.y == fim.y) print(pos, "green");
            else print(pos);
        }
        $("#myMap").append('<div class="line"></div>');
    }
    return true;
}
